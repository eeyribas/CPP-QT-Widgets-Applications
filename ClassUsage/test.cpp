#include "test.h"

double Test::SumCalc(double value_1, double value_2)
{
    return (value_1 + value_2);
}

double Test::SubstractCalc(double value_1, double value_2)
{
    return (value_1 - value_2);
}

double Test::MultipleCalc(double value_1, double value_2)
{
    return (value_1 * value_2);
}

double Test::DivideCalc(double value_1, double value_2)
{
    return (value_1 / value_2);
}
